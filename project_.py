LOWER_KEY_MAP = {k.lower(): k for k in MED_DB.keys()}

def best_med_match(user_med: str, cutoff=0.6):
    if not user_med or not user_med.strip():
        return None
    q = user_med.strip().lower()
    candidates = list(LOWER_KEY_MAP.keys())
    matches = get_close_matches(q, candidates, n=1, cutoff=cutoff)
    if matches:
        return LOWER_KEY_MAP[matches[0]]
    return None

def check_med_suitability(med_key: str, age: int, health_conditions: list, disease_to_treat: str):
    entry = MED_DB[med_key]
    indications = [i.lower() for i in entry.get("indications", [])]
    contraindications = [c.lower() for c in entry.get("contraindications", [])]
    disease_ok = False
    if disease_to_treat and disease_to_treat.strip():
        disease_ok = disease_to_treat.strip().lower() in indications
    reasons = []
    for hc in health_conditions:
        hc_l = hc.strip().lower()
        for contra in contraindications:
            if contra in hc_l or hc_l in contra:
                reasons.append(f"Contraindicated: {contra}")
    is_suitable = len(reasons) == 0
    return {"is_suitable": is_suitable, "treats_disease": disease_ok, "reasons": reasons}

