def generate_qr_image(link: str):
    link = (link or "").strip()
    if not link:
        return None, "Enter a valid Gradio share URL (e.g., https://xxxxx.gradio.live)."
    if not QRCODE_AVAILABLE:
        return None, "qrcode library not installed. Run: pip install qrcode[pil] and re-run."
    try:
        img = qrcode.make(link)
        bio = io.BytesIO()
        img.save(bio, format="PNG")
        bio.seek(0)
        return Image.open(bio), "QR generated. Right-click to save or use the Download button."
    except Exception as e:
        return None, f"QR generation error: {e}"

