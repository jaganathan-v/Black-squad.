def medicine_checker(med_input: str, age: int, health_conditions: str, disease_to_treat: str):
    meds = [m.strip() for m in (med_input or "").split(",") if m.strip()]
    health_list = [h.strip() for h in (health_conditions or "").split(",") if h.strip()]
    disease_clean = disease_to_treat.strip().lower() if disease_to_treat else ""
    if not meds:
        return "<div class='card'>⚠️ Please enter at least one medicine.</div>"

    time.sleep(0.35)  # short UX delay
    cards = []
    for med in meds:
        match = best_med_match(med)
        if not match:
            cards.append(f"<div class='card'><b>{med}</b><br><span style='color:#c93b3b;'>Medicine not found in local DB. Try different spelling or ask AI Doctor.</span></div>")
            continue
        suitability = check_med_suitability(match, age or 0, health_list, disease_clean)
        card_html = format_med_html_card(match, suitability, disease_clean, med)
        cards.append(card_html)
    tip = random.choice([
        "Tip: For liver/kidney conditions, always check contraindications before taking analgesics.",
        "Tip: If you have multiple medicines, check interactions separately with a clinician."
    ])
    cards.append(f"<div style='color:#475569;font-style:italic;margin-top:6px'>{tip}</div>")
    return "<div>" + "".join(cards) + "</div>"

