def ai_doctor_strict(user_text: str):
    t = (user_text or "").strip().lower()
    if "headache" in t:
        return ("Condition: The user is experiencing a headache. "
                "Recommendations: 1. Rest and relaxation. 2. Hydration. 3. Over-the-counter pain relievers such as ibuprofen or acetaminophen. "
                "Warnings: 1. If the headache persists or worsens, seek medical attention. 2. Avoid alcohol and caffeine, which can exacerbate headaches. "
                "Follow-up: 1. If the headache does not improve within a few days, schedule an appointment with a healthcare provider. 2. If the headache is accompanied by other symptoms such as fever, stiff neck, or confusion, seek immediate medical attention.")
    if "fever" in t:
        return ("Condition: The user has a fever. "
                "Recommendations: 1. Rest and fluids. 2. Paracetamol (acetaminophen) or ibuprofen as per dosing instructions for age. 3. Monitor temperature and hydration. "
                "Warnings: 1. Seek medical care if temperature is high (>39°C) or if the patient appears very unwell. 2. Avoid aspirin in children/teens. "
                "Follow-up: 1. Reassess after 24-48 hours or sooner if symptoms worsen. 2. Seek urgent care for breathing difficulty, severe lethargy, rash, or persistent vomiting.")
    if "cough" in t or "throat" in t or "sore throat" in t:
        return ("Condition: The user reports cough and/or sore throat. "
                "Recommendations: 1. Rest and stay hydrated. 2. Use warm fluids and throat lozenges. 3. Use OTC analgesics for pain/fever if appropriate. "
                "Warnings: 1. Seek medical care if breathing difficulty, high fever, or if cough produces blood. 2. Avoid antibiotics unless prescribed. "
                "Follow-up: 1. If cough lasts more than 7-10 days or worsens, consult a clinician. 2. Seek immediate care for severe breathlessness.")
    if "stomach" in t or "abdominal" in t or "nausea" in t or "vomit" in t:
        return ("Condition: The user has abdominal/stomach symptoms. "
                "Recommendations: 1. Rest, small sips of fluids, and bland diet. 2. Use anti-nausea measures as tolerated. 3. Avoid heavy/fatty meals until recovery. "
                "Warnings: 1. Seek urgent care if severe pain, blood in stool/vomit, persistent fever, or dehydration. 2. Avoid NSAIDs if ulcers or GI bleeding risk. "
                "Follow-up: 1. See a clinician if symptoms persist >48 hours or worsen. 2. Obtain immediate care for severe, escalating pain.")
    return ("Condition: General symptom query. "
            "Recommendations: 1. Rest and stay hydrated. 2. Use symptomatic OTC medicines as appropriate (e.g., paracetamol for fever). 3. Monitor your symptoms. "
            "Warnings: 1. If symptoms worsen, or if you have severe signs (difficulty breathing, chest pain, confusion, decreased urine), seek immediate medical attention. "
            "Follow-up: 1. Reassess after 24-72 hours. 2. Consult a healthcare provider for persistent or worsening symptoms.")

