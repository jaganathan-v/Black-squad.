# AI Chat (short, human-friendly replies) - returns updated history only
# -----------------------------
def ai_chat_short(user_text: str, history):
    user_text = (user_text or "").strip()
    history = history or []
    if not user_text:
        return history

    t = user_text.lower()
    if "headache" in t:
        reply = "Likely tension or migraine. Rest, hydrate, and consider paracetamol or ibuprofen if suitable. See doctor if severe, persistent, or with fever/neurological signs."
    elif "fever" in t:
        reply = "Fever likely viral; rest, fluids, paracetamol for comfort. See doctor for high fever or severe symptoms."
    elif "cough" in t:
        reply = "Try rest, fluids, steam inhalation, and OTC meds for symptom relief. See doctor if breathing is difficult or persistent."
    else:
        if ai_pipe:
            try:
                out = ai_pipe(user_text, max_new_tokens=40, temperature=0.3)[0]["generated_text"]
                generated = out.replace(user_text, "").strip()
                reply = generated.split("\n")[0].strip() or "Sorry — couldn't create a concise answer; try rephrasing."
            except Exception:
                reply = "Rest, hydrate, and monitor. Consult a clinician if severe or worsening."
        else:
            reply = "Rest, hydrate, and monitor. Consult a clinician if severe or worsening."

    history.append((user_text, reply))
    return history

